export function buildSoraStoryUserMessage(
    userEntry: string,
    soraStorySummary: string
): string {
    const parts: string[] = [];

    if (soraStorySummary) {
        parts.push(`これまでの「私」の記録:\n${soraStorySummary}`);
    }
    
    parts.push(`今日、届いたメッセージ（ユーザーの心象）:\n${userEntry}`);
    parts.push("今日のソラの物語を続けてください。ユーザーの感情を物語の比喩として変換し、新しい視点を提示してください。");

    return parts.join("\n\n");
}
