export function buildParallelStoryUserMessage(
  userEntry: string,
  storySummary: string
): string {
  const parts: string[] = [];

  if (storySummary) {
    parts.push(`これまでの物語の概要:\n${storySummary}`);
    parts.push(`今日のユーザーの気持ち・出来事:\n${userEntry}`);
    parts.push("今日の陽菜の章を書いてください。前の章からの続きとして自然につなげてください。");
  } else {
    parts.push(`今日のユーザーの気持ち・出来事:\n${userEntry}`);
    parts.push(
      "これは物語の第1章です。陽菜という人物を読者（ユーザー）に初めて紹介してください。" +
      "今日のユーザーの感情テーマを物語のメタファーとして描き、陽菜の気づきを通じてユーザーに新しい視点を提示してください。"
    );
  }

  return parts.join("\n\n");
}
